import type { Metadata } from "next";
import "./globals.css";
import { inter } from "./utils/style/fonts";
import { Profile } from "./feature/profile/ui/Profile";
import { loadProfileForGuestRequest } from "./feature/profile/business/profile.service";
import { presentProfile } from "./feature/profile/presentation/profile.presenter";

export const metadata: Metadata = {
  title: "IAMYOUNGHUN",
  description: "Generated by Young-Heon",
};

export default async function RootLayout({
  children,
}: {
  children: React.ReactNode;
}) {
  const profileResponse = await loadProfileForGuestRequest();
  const profile = profileResponse.data
    ? presentProfile(profileResponse.data)
    : null;

  return (
    <html lang="en">
      <body
        className={`${inter.className} bg-white pt-10 px-2 flex flex-col items-center`}
      >
        {profile ? <Profile {...profile} /> : null}
        {children}
      </body>
    </html>
  );
}
